package uf.seniorproj.alexryan;

public class Point {
	
	int x, y;
	
	public void setX (int x){
		this.x = x;
	}
	
	public void setY (int y){
		this.y = y;
	}
	
	public Integer getX(){
		return x;
	}
	
	public Integer getY(){
		return y;
	}
	
	public Point (int x, int y){
		this.x = x;
		this.y = y;
	}

}
